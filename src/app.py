import math
def add (a, b):
    return a+b

def sub (a, b):
    return a-b

def mult (a, b):
    return a*b

def div (a,b):
    return a/b

def log(a):
    return math.log10(a)

def square(a):
    return a * a

def sin(a):
    return math.sin(math.radians(a))

def cos(a):
    return math.cos(math.radians(a))

def sqrt(a):
    return math.sqrt(a)

def percentage(a, b):
   return (a*b) / 100
